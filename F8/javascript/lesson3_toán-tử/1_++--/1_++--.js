var number = 1
console.log(number++) //1
console.log(number) //2
console.log(number++) //2
console.log(number) //3

// => x++ tăng giá trị biến lên 1 và trả về giá trị trước khi tăng 

var số = 1
console.log(++số) //2
console.log(số) //2
console.log(++số) //3
console.log(số) //3

// => ++x tăng giá trị biến lên 1 và trả về giá trị sau khi tăng 